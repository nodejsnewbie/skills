def process_word(template_name, output_name, text_to_append):
    word = win32com.client.Dispatch("Word.Application")
    word.Visible = False
    try:
        doc = word.Documents.Open(os.path.join(resources_dir, template_name))
        selection = word.Selection
        selection.EndKey(Unit=6) # wdStory
        selection.TypeParagraph()
        selection.TypeText(Text=text_to_append)
        doc.SaveAs(os.path.join(output_dir, output_name))
        doc.Close()
    except Exception as e:
        print(f"Error processing {template_name}: {e}")
    finally:
        word.Quit()

def process_excel(template_name, output_name):
    shutil.copyfile(os.path.join(resources_dir, template_name), os.path.join(output_dir, output_name))

